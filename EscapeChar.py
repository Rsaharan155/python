###Types of Escape Sequence: Escape characters can be classified as non-printable characters when backslash precedes them. The print statements do not print escape characters.
#Example1:
a = "Anu\tSaharan"
print(a)

b = "Anuu\bSaharan"
print(b)

c = "Anu\nSaharan"
print(c)

d = "Anu\\Saharan"
print(d)